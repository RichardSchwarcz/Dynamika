def elasticModulus(concrete):
    if concrete == "C16/20":
        return 2.9 * (10**7)
    if concrete == "C20/25":
        return 3.0 * (10**7)
    if concrete == "C25/30":
        return 3.1 * (10**7)
    if concrete == "C30/37":
        return 3.3 * (10**7)
    if concrete == "C35/45":
        return 3.4 * (10**7)
